

public class OpMaths extends Operation {

		
	public Integer plus(Integer a, Integer b) {
		return a+b;
	}
	
	public Float plus(Float a, Float b) {
		return a+b;
	}
	
	public Double plus(Double a, Double b) {
		return a+b;
	}
	
	public Integer moins(Integer a, Integer b) {
		return a-b;
	}
	
	public Float moins(Float a, Float b) {
		return a-b;
	}
	
	public Double moins(Double a, Double b) {
		return a-b;
	}
	
	public Integer multi(Integer a, Integer b) {
		return a*b;
	}
	
	public Float multi(Float a, Float b) {
		return a*b;
	}
	
	public Double multi(Double a, Double b) {
		return a*b;
	}
	
	public Integer divise(Integer a, Integer b) {
		return a/b;
	}
	
	public Float divise(Float a, Float b) {
		return a/b;
	}
	
	public Double divise(Double a, Double b) {
		return a/b;
	}

	public Integer sin(Integer x) {
		return (int)Math.sin(x);
	}
	
	public Float sin(Float x) {
		return (float)Math.sin(x);
	}
	
	public Double sin(Double x) {
		return Math.sin(x);
	}
	
	public Integer cos(Integer x) {
		return (int)Math.cos(x);
	}
	
	public Float cos(Float x) {
		return (float)Math.cos(x);
	}
	
	public Double cos(Double x) {
		return Math.cos(x);
	}
	
	public Integer tan(Integer x) {
		return (int)Math.tan(x);
	}
	
	public Float tan(Float x) {
		return (float)Math.tan(x);
	}
	
	public Double tan(Double x) {
		return Math.tan(x);
	}
}
